package com.test1.test2.Bean;

public class ScoreBean {
    private String no;
    private String name;
    private int jspScore;
    private int xmlScore;
    private int aspScore;

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getJspScore() {
        return jspScore;
    }

    public void setJspScore(int jspScore) {
        this.jspScore = jspScore;
    }

    public int getXmlScore() {
        return xmlScore;
    }

    public void setXmlScore(int xmlScore) {
        this.xmlScore = xmlScore;
    }

    public int getAspScore() {
        return aspScore;
    }

    public void setAspScore(int aspScore) {
        this.aspScore = aspScore;
    }
}
